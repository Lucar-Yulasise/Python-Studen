# print：输出语句
# 作用：将信息在控制台上打印

# 打印一个字符串
print("我爱学习")

# 打印多条数据时，用 , (逗号)隔开，遇到逗号会有一个空格
print("学习是我快乐", "为什么快乐", "因为所以")

# 打印一个数字
print(123)

# 混合打印
print(123, "科学道理")

# 可以执行相应的数学运算
print(1 + 2)

print("1 + 2")

# print()   默认会自动换行
# 如果输出时不想换行，在print中增加一个end参数
# end：设置当前输出语句以什么结尾，end的数据类型为字符串类型,
# 默认end后面的内容为\n
print("hello", end="***")
print("world", end="\n")
print("！")


